drop table if exists TB_LIST CASCADE;
drop table if exists TASKS CASCADE;
create table TB_LIST (list_id integer generated by default as identity,
 created_at varchar(255),
 list_name varchar(30) not null,
 primary key (list_id));
create table TASKS (task_id integer generated by default as identity,
 created_at varchar(255) not null,
 task varchar(255) not null,
 task_completed boolean not null,
 fk_list_id integer,
 foreign key (fk_list_id) references TB_LIST,
 primary key (task_id)
 );
 


